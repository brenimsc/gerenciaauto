    public void lerJTable() throws SQLException {
        DefaultTableModel modelo = (DefaultTableModel) jTablePeca.getModel();
        modelo.setNumRows(0);
        Connection conexao = new Conexao().getConnection();
        PecaDAO pecadao = new PecaDAO(conexao);
        
        for (Peca pec: pecadao.ler()) {
            
            modelo.addRow(new Object[]{
                pec.getId(),
                pec.getDescricao(),
                pec.getMarca(),
                pec.getValor(),
                pec.getQtd()
            });
            
        }
        
        
    }
    
