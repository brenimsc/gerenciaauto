    private void jButtonEditarEstoqueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarEstoqueActionPerformed
          if(jTablePeca.getSelectedRow() != -1) { //Verificando se esta selecionando a linha
        
           //controller.atualizaCliente();
           //String teste = jTableClientes2.getValueAt(jTableClientes2.getSelectedRow(), 2).toString();
           
        String descricao = getjTextDescEstoque().getText(); //Tive que criar getter e setter pra poder acessar os campos da view
        String marca = getjTextMarcaEstoque().getText();
        String valorString = getjTextValorEstoque().getText();
        Float valor = Float.parseFloat(valorString);
        int id = ((int)jTablePeca.getValueAt(jTablePeca.getSelectedRow(), 0));
        String qtdString = getjTextQtd().getText();
        int qtd = Integer.parseInt(qtdString);

        
        Peca peca = new Peca(id, descricao, marca, valor, qtd);
       
       
        try {
            Connection conexao = new Conexao().getConnection();
            PecaDAO servicodao = new PecaDAO(conexao);
            servicodao.update(peca);
            
            JOptionPane.showMessageDialog(null, "Serviço Atualizado!");
            
        } catch (SQLException ex) {
            //Logger.getLogger(CadCliente.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro ao atualizar!"+ex);
        }
              try {
                  lerJTable();
              } catch (SQLException ex) {
                  Logger.getLogger(TelEstoque.class.getName()).log(Level.SEVERE, null, ex);
              }
       }
          limparTela();
    }//GEN-LAST:event_jButtonEditarEstoqueActionPerformed

